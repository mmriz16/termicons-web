function iconSearch() {
    const inputSearch = document.getElementById("myInput").value;
    const allIcon = document.querySelectorAll(`i[class*='ti-icon-style']`);
    if (inputSearch.trim() == "") {
        allIcon.forEach((element) => {
            const parent = element.parentNode;
            parent.parentNode.classList.remove("icon-hidden");
        });
        document.getElementById("iconsTotal").innerText = `${Math.floor(
            allIcon.length / 2
        )} of ${Math.floor(allIcon.length / 2)} icons`;
        return;
    }
    const iconFiltered = document.querySelectorAll(
        `i[class*="${inputSearch}"]`
    );
    allIcon.forEach((element) => {
        const parent = element.parentNode;
        parent.parentNode.classList.add("icon-hidden");
    });
    iconFiltered.forEach((element) => {
        const parent = element.parentNode;
        parent.parentNode.classList.remove("icon-hidden");
    });

    document.getElementById("iconsTotal").innerText = `${Math.floor(
        iconFiltered.length / 3
    )} of ${Math.floor(allIcon.length / 2)} icons`;
}

